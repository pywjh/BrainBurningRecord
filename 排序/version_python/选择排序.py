# 这个版本比较容易理解
# 但是空间复杂度相比版本1
# 版本1：O(1)；版本2：O(n)
def main2(ary):
    """
    选择排序

        第0次遍历
        new []
        34, 22, 54, 49, 91
        --------------------- min: 22
        new [22]
        34, 54, 49, 91
        --------------------- min: 34
        new [22, 34]
        54, 49, 91
        --------------------- min: 49
        new [22, 34, 49]
        54, 91
        --------------------- min: 54
        new [22, 34, 49, 54]
        91
        --------------------- min: 91
        new [22, 34, 49, 54, 91]
    """
    def find_min(ary):
        min_num = float('inf')
        min_index = 0
        for i, v in enumerate(ary):
            if v < min_num:
                min_num = v
                min_index = i
        return min_index
    res = []
    while ary:
        min_index = find_min(ary)
        res.append(ary.pop(min_index))

    return res  
    
